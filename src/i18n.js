let currentLang = 'en';

export function setLang(lang) {
  currentLang = LANGS[lang] ? lang : 'en';
  localStorage.setItem('illuminati_lang', currentLang);
}

export function getLang() {
  return currentLang;
}

export function initLang() {
  const saved = localStorage.getItem('illuminati_lang');
  if (saved && LANGS[saved]) currentLang = saved;
  else {
    const browserLang = navigator.language || navigator.userLanguage || '';
    if (browserLang.startsWith('ru')) currentLang = 'ru';
  }
}

export function t(key) {
  return LANGS[currentLang][key] || LANGS.en[key] || key;
}

